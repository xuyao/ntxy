package com.world.model.entitys;

/**
 * 委托订单
 * @author guosj
 */
public class Order {
	
	//挂单ID
	private String id;
	//挂单类型 1/0 buy/sell
	private int type;
	//单价
	private double price;
	//挂单货币类型 btc/ltc
	private String currency;
	//已成交数量
	private double trade_amount;
	//已成交总金额
	private double trade_money;
	//挂单总数量
	private double total_amount;
	//挂单交易日期
	private long trade_date;
	//挂单状态
	private int status;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	
	public double getTrade_amount() {
		return trade_amount;
	}
	public void setTrade_amount(double tradeAmount) {
		trade_amount = tradeAmount;
	}
	public double getTotal_amount() {
		return total_amount;
	}
	public void setTotal_amount(double totalAmount) {
		total_amount = totalAmount;
	}
	public long getTrade_date() {
		return trade_date;
	}
	public void setTrade_date(long tradeDate) {
		trade_date = tradeDate;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public double getTrade_money() {
		return trade_money;
	}
	public void setTrade_money(double trade_money) {
		this.trade_money = trade_money;
	}
}
